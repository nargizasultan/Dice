package com.company;

public interface Sides {
    public static void firstSide(){
        System.out.println("+------+");
        System.out.println("+------+");
        System.out.println("+---#--+");
        System.out.println("+------+");
        System.out.println("+------+");
    }
    public static void secondSide(){
        System.out.println("+------+");
        System.out.println("+-#----+");
        System.out.println("+------+");
        System.out.println("+----#-+");
        System.out.println("+------+");
    }
    public static void thirdSide(){
        System.out.println("+------+");
        System.out.println("+#-----+");
        System.out.println("+---#--+");
        System.out.println("+-----#+");
        System.out.println("+------+");
    }
    public static void forthSide(){
        System.out.println("+------+");
        System.out.println("+#----#+");
        System.out.println("+------+");
        System.out.println("+#----#+");
        System.out.println("+------+");
    }
    public static void fifthSide(){
        System.out.println("+------+");
        System.out.println("+#----#+");
        System.out.println("+---#--+");
        System.out.println("+#----#+");
        System.out.println("+------+");
    }
    public static void sixthSide(){
        System.out.println("+-------+");
        System.out.println("+#--#--#+");
        System.out.println("+-------+");
        System.out.println("+#--#--#+");
        System.out.println("+-------+");
    }

}
